def generate_page_content(page_num, yiji, erji, page_name):
    """生成单个页面的内容"""
    module = page_module_mapping.get(page_name)
    content = module_content.get(module, {})

    # 检查是否需要补充详细设计
    need_supplement = '【待补充详细设计】' in content.get('功能目标', '')

    section = f"""## 4.2.{page_num} {yiji}-{erji}-{page_name}

**功能目标：** {content.get('功能目标', '【需补充详细设计】')}

**核心功能点：**
"""

    if '核心功能点' in content:
        for point in content['核心功能点']:
            section += f"1. {point}\n"
    else:
        section += "【需补充详细设计】\n"

    section += f"""
**业务流程：**
"""

    if '业务流程' in content and not content['业务流程'].startswith('【待补充'):
        section += f"```mermaid\nflowchart TD\n"
        # 简单处理业务流程文本
        lines = content['业务流程'].split('\n')
        for i, line in enumerate(lines):
            if line.strip():
                section += f"    {line.strip()}\n"
        section += "```\n"
    else:
        section += "【需补充详细设计】\n"

    section += f"""
**数据结构设计：**

"""

    if '数据表' in content:
        section += "| 表名 | 说明 |\n|------|------|\n"
        for table in content['数据表']:
            section += f"| {table} | 【需补充表说明】 |\n"
    else:
        section += "【需补充详细设计】\n"

    section += f"""
**权限设计：**

| 权限项 | 角色 |
|--------|------|
| 查看 | {content.get('权限', '【需补充】')} |
| 编辑 | 【需补充】 |

"""

    if need_supplement:
        section += "> **注意：** 本页面内容【需补充详细设计】\n\n"

    section += "---\n\n"
    return section

